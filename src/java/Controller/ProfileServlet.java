    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        if (session.getAttribute("acc") == null) {
            response.sendRedirect("login.jsp");
        } else {
            User user = (User) session.getAttribute("acc");
            int id = user.getId();
            AccountDao ad = new AccountDao();
            User u = ad.GetAccountById(id);
            request.setAttribute("user", u);
            request.setAttribute("mess", session.getAttribute("mess"));
            request.getRequestDispatcher("profile.jsp").forward(request, response);
        }

    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        User acc = (User) session.getAttribute("acc");

        if (acc != null) {
            String fullName = request.getParameter("fullName");
            String phone = request.getParameter("phone");
            String address = request.getParameter("address");
            
            int id = acc.getId();
            AccountDao ad = new AccountDao();
            ad.UpdateProfileById(id, fullName, phone, address);
            session.setAttribute("mess", "Lưu thành công!!!");
            response.sendRedirect("profile");
            
            // Assume you have an AccountService to handle the update
        }else{
            response.sendRedirect("login.jsp");
        }

}

